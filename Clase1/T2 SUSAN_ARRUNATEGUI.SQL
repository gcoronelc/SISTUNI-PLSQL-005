-- T2: Desarrollar un procedimiento que retorne de una cuenta
--     su saldo y el nombre de la moneda. EUREKA.

CREATE OR REPLACE PROCEDURE EUREKA.PR_CTA_EMP 
( P_CTA IN VARCHAR2, P_SALDO OUT NUMBER, P_MONEDA OUT VARCHAR)
AS
BEGIN
	
	SELECT  cta.DEC_CUENSALDO,mo.VCH_MONEDESCRIPCION  
	INTO P_SALDO, P_MONEDA 
	FROM EUREKA.CUENTA cta INNER JOIN MONEDA mo ON mo.CHR_MONECODIGO = cta.CHR_MONECODIGO
	WHERE  CHR_CUENCODIGO = P_CTA;
END;

  
	
	
 
BEGIN
	EUREKA.PR_CTA_EMP ('00200001', :p_saldo$NUMBER, :p_moneda$VARCHAR2);
END;
	