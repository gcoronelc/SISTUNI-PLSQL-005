
DECLARE 
    V_NUM_TABLA NUMBER(5) := 2;
    V_CORRELATIVO NUMBER(8);
BEGIN
    SISCE.PKG_SCE_UTIL.PRC_GET_CORRELATIVO(V_NUM_TABLA,V_CORRELATIVO);
    DBMS_OUTPUT.PUT_LINE( 'CORRELATIVO: ' || V_CORRELATIVO );
END;
/


DECLARE 
    V_NUM_TABLA VARCHAR2(20);
    V_ARREGLO SISCE.PKG_SCE_UTIL.T_ARRAY_STRING;
BEGIN
    V_ARREGLO := SISCE.PKG_SCE_UTIL.SPLIT(' ',',');
    DBMS_OUTPUT.PUT_LINE('COUNT: ' || V_ARREGLO.COUNT);
    FOR I in 1 .. V_ARREGLO.COUNT LOOP 
      dbms_output.put_line(V_ARREGLO(I)); 
   END LOOP;
END;
/

