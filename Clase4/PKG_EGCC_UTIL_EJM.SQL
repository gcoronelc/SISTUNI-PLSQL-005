

DECLARE
 
	V_DATOS VARCHAR2(1000);
	V_ARREGLO SCOTT.PKG_EGCC_UTIL.T_ARRAY_STRING;
	
BEGIN

	V_DATOS := 'DATO1|DATO2|5678|GUSTAVO';
	V_ARREGLO := SCOTT.PKG_EGCC_UTIL.SPLIT(V_DATOS,'|');
	
	DBMS_OUTPUT.PUT_LINE('COUNT: ' || V_ARREGLO.COUNT);
