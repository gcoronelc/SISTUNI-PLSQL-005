CREATE OR REPLACE PROCEDURE SCOTT.FINDEMP(COD EMP.EMPNO%TYPE)
IS
    SALARIO EMP.SAL%TYPE;
BEGIN

	SELECT SAL INTO SALARIO
	FROM EMP
	WHERE EMPNO = COD;
	
	DBMS_OUTPUT.PUT_LINE ('SALARIO: ' || SALARIO);
	
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
			DBMS_OUTPUT.PUT_LINE ('CODIGO NO EXISTE.');	
	
END;



BEGIN
	SCOTT.FINDEMP (7369);
END;

BEGIN
	SCOTT.FINDEMP (6666);
END;
